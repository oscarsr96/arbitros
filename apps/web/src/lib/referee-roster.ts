function slug(s: string): string {
  return s
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '.')
    .replace(/(^\.|\.$)/g, '')
}

// Pool de nicks únicos: SOLO palabras sueltas (una palabra, MAYÚSCULAS, solo
// letras), barajadas de forma determinista y deduplicadas con Set. Debe tener
// ≥1300 entradas únicas (se consumen 1270: 770 árbitros + 500 anotadores).
// Exportada para que los tests validen tamaño y restricciones del pool.
export function buildNickPool(rand: () => number): string[] {
  const seen = new Set<string>()
  const pool: string[] = []
  for (const nick of shuffle([...LUGARES, ...APODOS], rand)) {
    if (!seen.has(nick)) {
      seen.add(nick)
      pool.push(nick)
    }
  }
  return pool
}

