        phone,
        role: 'arbitro',
        category: LEGACY_CATEGORY_BY_LEVEL[level],
        address: `${calle} ${num}, ${cp} ${muni.name}`,
        postalCode: cp,
        municipalityId: muni.id,
        bankIban: `ES${ibanBody}`,
